#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	size_t	size;
	size_t	i;

	size = ft_strlen(s) + 1;
	p = (char *)malloc(size * sizeof(char));
	if (!p)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
